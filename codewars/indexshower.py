def print_array(arr, row):
    width = len(row)
    height = len(arr) // width

    for i in range(height):
        print(arr[i * width:width * (i + 1)])



def unpack_array(bad_arr):
    new_arr = []

    for i in bad_arr:
        for j in i:
            new_arr.append(j)


    return new_arr



